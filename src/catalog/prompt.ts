    '    "shell": { "type": "Container", "props": { "maxWidth": "md", "padding": "lg" }, "children": ["header", "tabs", "content"] },',
    '    "header": { "type": "Stack", "props": { "direction": "horizontal", "align": "center", "spacing": "md" }, "children": ["title", "streak-metric"] },',
    '    "title": { "type": "Text", "props": { "content": "Daily Habits", "variant": "heading1" } },',
    '    "streak-metric": { "type": "Metric", "props": { "label": "Best Streak", "value": "$computed:habits/maxStreak", "format": "number", "suffix": " days" } },',
    '    "tabs": { "type": "Tabs", "props": { "items": ["Today", "All Habits", "Add New"], "bind": "$state:view/active" } },',
    '    "content": { "type": "Stack", "props": { "spacing": "md" }, "children": ["habit-list", "empty"] },',
    '    "habit-list": { "type": "Repeater", "props": { "dataPath": "habits", "template": "habit-row" } },',
    '    "habit-row": { "type": "Card", "props": { "variant": "outlined", "padding": "md" }, "children": ["habit-info", "habit-toggle"] },',
    '    "habit-info": { "type": "Stack", "props": { "direction": "horizontal", "align": "center", "spacing": "sm" }, "children": ["habit-icon", "habit-name", "habit-streak"] },',
