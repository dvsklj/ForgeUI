import { ALL_COMPONENT_TYPES, CATEGORY_MAP } from './registry.js';

/**
 * Catalog prompt generator — three tiers for LLM system prompt injection.
 *
 * - minimal  (~800 tokens)  — manifest structure + component names only
 * - default  (~2400 tokens) — key components + state bindings + design tokens
 * - full     (~5000 tokens) — complete component reference from forgeui-llm-instructions.md
 */

export function catalogPrompt(tier: 'minimal' | 'default' | 'full' = 'default'): string {
  switch (tier) {
    case 'minimal': return buildMinimalPrompt();
    case 'default': return buildDefaultPrompt();
    case 'full': return buildFullPrompt();
  }
}

function buildMinimalPrompt(): string {
  const byCat = CATEGORY_MAP;
  const cats = Object.entries(byCat)
    .map(([cat, types]) => `  ${cat}: ${types.join(', ')}`)
    .join('\n');

  return [
    'You can create interactive web applications by generating Forge manifests — declarative JSON that describes UI, data, and behavior. You never write code.',
    '',
    'MANIFEST STRUCTURE:',
    '{',
    '  "manifest": "0.1.0",',
    '  "id": "my-app",',
    '  "root": "root-element-id",',
    '  "state": { "key": "initial value" },',
    '  "elements": {',
    '    "root-element-id": {',
    '      "type": "Stack",',
    '      "props": { "spacing": "md" },',
    '      "children": ["child1", "child2"]',
    '    }',
    '  },',
    '  "actions": { "actionName": { "type": "mutateState", "path": "state/path" } }',
    '}',
    '',
    'ELEMENT FORMAT: { "type": "ComponentName", "props": {}, "children": ["id"] }',
    'Root element is set in "root" field. All elements are a flat map — reference by ID, never nest.',
    '',
    'COMPONENTS (37 types):',
    cats,
    '',
    'STATE BINDINGS:',
    '  $state:path         — reactive value',
    '  $computed:path       — derived from table data',
    '  $item:field          — current item in Repeater',
    '  $form:fieldId        — value from input component',
    '',
    'DESIGN TOKENS:',
    '  Spacing: xs, sm, md, lg, xl',
    '  Colors: primary, secondary, success, warning, error, muted, default',
    '  Sizes: sm, md, lg',
    '  Radius: none, sm, md, lg, full',
    '',
    'Never use raw CSS values, hex colors, or pixel sizes. Always use tokens.',
    '',
    'EXAMPLE:',
    '{',
    '  "manifest": "0.1.0", "id": "hello", "root": "root",',
    '  "elements": {',
    '    "root": { "type": "Card", "props": { "title": "Hello World" }, "children": ["msg", "btn"] },',
    '    "msg": { "type": "Text", "props": { "content": "Welcome to Forge!", "variant": "heading2" } },',
    '    "btn": { "type": "Button", "props": { "label": "Click Me", "action": "greet" } }',
    '  },',
    '  "actions": { "greet": { "type": "setState", "path": "greeting", "value": "Hello!" } }',
    '}',
  ].join('\n');
}

